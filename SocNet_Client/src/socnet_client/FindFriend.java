    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //Checks indexes are valid, adds the selected friend.
        if(jList1.getSelectedIndex() <= jList1.getMaxSelectionIndex() && 
            jList1.getSelectedIndex() >= jList1.getMinSelectionIndex())
        {
            String[] person = results.get(jList1.getSelectedIndex());
            Process.addFriend(Integer.parseInt(person[1]));
        }
    }//GEN-LAST:event_jButton3ActionPerformed
    private void updateList(ArrayList data)
    {
        //Updates the list using the data returned by the search
        DefaultListModel listModel = new DefaultListModel();
        results.clear();
        if(data.isEmpty())
        {
            listModel.addElement("No results found.");
        }
        else
        {
            /*
                For each person returned create an array using their unique 
                userID an their username, add this to the listbox for returns.
            */
            for(int i = 0; i < data.size(); i += 2)
            {
                String[] person = { data.get(i).toString(), 
                    data.get(i+1).toString()};
                results.add(person);
                listModel.addElement(data.get(i));
            }
        }
        jList1.setModel(listModel);
    }
